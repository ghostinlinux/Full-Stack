// array push
// add element in last
let course = ["CSE","PHARMACY","NURSING"]
course.push("BCA")
console.log(course)

// array pop
// remove element from last
let collage = ["CU","CHANDIGHAR UNI","LPU","SRM"]
collage.pop()
console.log(collage)

// array unshift
// add element in first

let fruits = ["banana","mango","litchi"];
fruits.unshift("pine")
console.log(fruits)

// array shift
// remove element from first

let fruits1 = ["banana","mango","litchi"];
fruits1.shift()
console.log(fruits1)

